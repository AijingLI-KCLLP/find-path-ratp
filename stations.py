# Nombre total de stations
N = len(STATIONS)

# Dictionnaire pour accès rapide par nom
NAME_TO_ID = {s["name"]: s["id"] for s in STATIONS}


def get_station(station_id):
    """Retourne les informations d'une station par son ID"""
    if 0 <= station_id < N:
        return STATIONS[station_id]
    return None


def get_station_by_name(name):
    """Retourne les informations d'une station par son nom"""
    station_id = NAME_TO_ID.get(name)
    return STATIONS[station_id] if station_id is not None else None


def get_stations_by_line(line):
    """Retourne la liste des stations d'une ligne donnée"""
    return [s for s in STATIONS if line in s["lines"]]
